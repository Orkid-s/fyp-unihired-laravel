    function showRegisterForm(){
        return view('auth.register');
    }

    function redirectRegisteredUser(Request $request){
        $this->validator($request->all())->validate();
        event(new Registered($user = $this->register($request->all())));
        $this->guard()->login($user);

        //redirect the registered lecturer to personal info page, with logged in 

        if($user->save() && $user->role==1){
            return $this->registered($request, $user)
            ?: 
                redirect()->route('lecturer.profile.completion');
        }
        elseif($user->save() && $user->role==2){
            return $this->registered($request, $user)
            ?: 
                redirect()->route('student.profile.completion');
        }
        else{
            //if not registered successfully, return back to register page 
            return redirect()->back();
        }  
        
    }


    
